public class BaiTap1 {
    public static void main(String[] args) {
         Scanner sc = new Scanner(System.in);
        int soNguyen1, soNguyen2;
        System.out.print("Enter the first interger: ");
        soNguyen1 = sc.nextInt();
        System.out.print("Enter the second interger: ");
        soNguyen2 = sc.nextInt();
        int tong = soNguyen1 + soNguyen2;
        int hieu = soNguyen1 - soNguyen2;
        int tich = soNguyen1 * soNguyen2;
        float thuong = (float)soNguyen1 / soNguyen2;
        int chia_lay_du = soNguyen1 % soNguyen2;
        System.out.println("Addition results: " +tong );
        System.out.println("Subtraction results: " +hieu );
        System.out.println("Multiplication results: " +tich);
        System.out.println("Division results: " +thuong);
        System.out.println("Division to get remainder results: " +chia_lay_du);
    }
}
